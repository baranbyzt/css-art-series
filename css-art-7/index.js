


function onclickitem_1() {
   
let click_true = document.getElementsByClassName('main_check')[0];
if(click_true.style.visibility == "visible"){
    click_true.style.visibility= "hidden";
}else{
    click_true.style.visibility= "visible"; 
}

let items = document.getElementById("itemss_1");

if(items.style.border == ""){
 items.style.border=" 2px rgb(163, 0, 238) solid";
}else{
    items.style.border = "";
}



}
